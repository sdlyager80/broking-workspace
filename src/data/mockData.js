export const getSlaStatusColor = (status) => {
  switch (status) {
    case "On Track": return "#24A148";
    case "At Risk": return "#FF6B00";
    case "SLA Critical": return "#FF6B00";
    case "Breached": return "#D0021B";
    default: return "#0095FF";
  }
};

